    public void carregarFuncionarios() {
        modelo.setRowCount(0);

        for (Funcionario f : FuncionarioRepositorio.getListaFuncionarios()) {
            modelo.addRow(new Object[]{
                f.getNome(),
                f.getCargo(),
                f.getCpf(),
                f.getTelefone(),
                f.getEndereco(),
                f.getBairro(),
                f.getCidade(),
                f.getDataNascimento()
            });
        }
    }

