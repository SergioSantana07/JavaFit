    private void DeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeletarActionPerformed
        int linhaSelecionada = jTable1.getSelectedRow();

        if (linhaSelecionada == -1) {
            JOptionPane.showMessageDialog(this, "Por favor, selecione um funcionário para deletar.");
            return;
        }

        
        Funcionario funcionario = FuncionarioRepositorio.getListaFuncionarios().get(linhaSelecionada);

        
        int opcao = JOptionPane.showConfirmDialog(
                this,
                "Tem certeza que deseja deletar o funcionário " + funcionario.getNome() + "?",
                "Confirmar Exclusão",
                JOptionPane.YES_NO_OPTION
        );

        if (opcao == JOptionPane.YES_OPTION) {
            
            FuncionarioRepositorio.removerFuncionario(funcionario);

            
            carregarFuncionarios();

            
            JOptionPane.showMessageDialog(this, "Funcionário deletado com sucesso.");
        }
    }//GEN-LAST:event_DeletarActionPerformed

