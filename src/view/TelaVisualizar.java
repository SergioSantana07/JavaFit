    private void jButton1_EditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1_EditarActionPerformed
        
        int linhaSelecionada = jTable1.getSelectedRow();

        if (linhaSelecionada == -1) {
            JOptionPane.showMessageDialog(this, "Selecione um funcionário para editar.");
            return;
        }

        Funcionario funcionarioSelecionado = FuncionarioRepositorio.getListaFuncionarios().get(linhaSelecionada);

        
        TelaEditar telaEditar = new TelaEditar(funcionarioSelecionado, linhaSelecionada);
        telaEditar.setVisible(true);
        dispose();

    }//GEN-LAST:event_jButton1_EditarActionPerformed

